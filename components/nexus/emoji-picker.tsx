export function EmojiPicker({ onEmojiSelect, children }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState('Smileys & People');
  const [open, setOpen] = useState(false);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setOpen(false);
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        {children}
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0" align="start">
        <div className="border-b border-slate-200">
          <div className="flex overflow-x-auto">
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <Button
                key={category}
                variant="ghost"
                size="sm"
                className={cn(
                  'flex-shrink-0 text-xs px-3 py-2 rounded-none border-b-2 border-transparent',
                  selectedCategory === category && 'border-blue-500 bg-blue-50'
                )}
                onClick={() => setSelectedCategory(category)}
              >
                {category.split(' ')[0]}
              </Button>
            ))}
          </div>
        </div>
        
        <ScrollArea className="h-64 p-3">
          <div className="grid grid-cols-8 gap-1">
            {EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES].map((emoji, index) => (
              <Button
                key={`${emoji}-${index}`}
                variant="ghost"
                size="sm"
                className="h-8 w-8 p-0 text-lg hover:bg-slate-100 rounded"
                onClick={() => handleEmojiClick(emoji)}
              >
                {emoji}
              </Button>
            ))}
          </div>
        </ScrollArea>
        
        <div className="border-t border-slate-200 p-2">
          <p className="text-xs text-slate-500 text-center">
            Click an emoji to add it to your message
          </p>
        </div>
      </PopoverContent>
    </Popover>
  );
}